    private void loadUser(int id) {

        user = new User(connection);
        String query = "select pk_id_usuario,nombre,apellido,email,telefono,progreso"
                + "alumno_111mil from usuario where pk_id_usuario=" + id;
        connection.executeQuery(query);
        ResultSet res = connection.getResultSet();
        try {
            user.setIdUser(res.getInt("pk_id_usuario"));
            user.setName(res.getString("nombre"));
            user.setSurname(res.getString("apellido"));
            user.seteMail(res.getString("email"));
            user.setTelephoneNumber(res.getString("telefono"));
            user.setProgress(res.getInt("progreso"));
            user.setIs111Student(res.getBoolean("alumno_111mil"));
        } catch (SQLException ex) {
            ex.printStackTrace();//ver si hay que hacer caso al warning del IDE (si optamos por
            //imprimir por consola el objeto, el warning desaparece).
            //en varias partes del codigo invoque a printStackTrace (ver si hay que modificar todo eso).
        }

    }

