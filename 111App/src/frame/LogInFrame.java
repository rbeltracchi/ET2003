    private boolean validateLogIn() throws SQLException {

        String userField = userTextField.getText();
        char[] passwordField = userPasswordField.getPassword();
        String password = null;
        for (Character c : passwordField) {

            password += c;

        }
        if ((connection.checkExistingValue(userField, "email", "usuario") && (connection.checkExistingValue(password, "contraseña", "usuario")))) {

            String query = "select pk_id_usuario from usuario where email=" + userField + " and contraseña=" + password;
            ResultSet res = connection.getResultSet();
            loadUser(res.getInt("pk_id_usuario"));
            return true;

        } else {

            return false;

        }

    }

